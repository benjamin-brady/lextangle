		<li>Messages you send us directly (for example via Discord)</li>
	</ul>

	<h3 class="font-bold pt-2">Information collected automatically</h3>
	<ul class="list-disc pl-5 space-y-1">
		<li>IP address, browser type, device information, and operating system</li>
		<li>Pages viewed, session timing, and basic activity signals</li>
		<li>Event data used for product analytics and diagnostics</li>
		<li>Cookies and local storage values used to save your progress</li>
	</ul>

