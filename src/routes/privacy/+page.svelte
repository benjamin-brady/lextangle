	<ul class="list-disc pl-5 space-y-1">
		<li>Run, secure, and improve the game</li>
		<li>Persist your daily progress, streaks, and settings</li>
		<li>Measure usage with analytics to improve puzzle quality and reliability</li>
		<li>Detect abuse and comply with legal obligations</li>
	</ul>

	<h2 class="font-display text-lg font-bold pt-2">3. Analytics</h2>
	<p>
		LexLink uses Google Analytics (GA4) to measure aggregate usage trends and product performance.
		This may set cookies and collect technical and usage data (for example page views, session
		behavior, browser/device details, and approximate location from IP).
	</p>
	<p>
		You can limit analytics tracking through browser privacy settings, content blockers, and cookie
		controls where available.
	</p>

	<h2 class="font-display text-lg font-bold pt-2">4. Local Storage</h2>
	<p>
		Your puzzle progress, theme preference, and tutorial state are stored locally in your browser.
		This data stays on your device and is not sent to our servers.
	</p>

	<h2 class="font-display text-lg font-bold pt-2">5. Sharing of Information</h2>
	<p>We do not sell personal information. Data may be shared only in limited cases:</p>
	<ul class="list-disc pl-5 space-y-1">
		<li>With infrastructure and analytics service providers (Cloudflare, Google Analytics)</li>
		<li>To comply with legal process or enforce our terms</li>
	</ul>

	<h2 class="font-display text-lg font-bold pt-2">6. Data Retention</h2>
	<p>
		Feedback submissions are retained only as long as needed to review and improve puzzles.
		Analytics data follows the retention periods configured in Google Analytics.
	</p>

